export async function getCountriesList() {
  const rawResponse = await fetch('/api/codelist/countries', {
    method: 'GET',
    credentials: 'same-origin',
    headers: {
      'Accept': 'application/json',
      'Content-Type': 'application/json',
    },
  })
  return rawResponse.json()
}

